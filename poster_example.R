# This is an exact copy of the script from our poster, with
# several minor modifications (to make it run in a cloud binder
# instance) or additions (we have a chance to show a little bit
# more there han we could in the poster). We have documented these
# changes in comments below.

# the package is already installed and we can simply load it
library(slendr)

# 1. spatial context ------------------------------------------------------

map <- world(xrange = c(-15, 60), yrange = c(20, 65),
             landscape = "naturalearth", crs = "EPSG:3035")

# 'plot' is our generic function which is able to plot all spatial
# objects created by this package
plot(map)

# geographic regions used later to define population boundaries
europe <- region("Europe", map = map, polygon = list(
    c(-8, 35), c(10, 38), c(20, 35), c(25, 35),
    c(32, 45), c(20, 58), c(-5, 60), c(-15, 50))
)
anatolia <- region("Anatolia", map = map, polygon = list(
  c(28, 35), c(40, 35), c(42, 40),
  c(30, 43), c(27, 40), c(25, 38))
)

plot(europe, anatolia)

# 2. population dynamics --------------------------------------------------

# in order to make it possible to run the whole simulation using the
# restricted resources of the free cloud binder instance we dramatically
# decreased the number of individuals in each population `N`

ehg <- population( # Eastern hunter-gatherers
  "EHG", time = 15000, N = 5000, map = map, remove = 6500,
  polygon = list(c(26, 55), c(38, 53), c(48, 53), c(60, 53),
                 c(60, 60), c(48, 63), c(38, 63), c(26, 60))
)
plot(ehg)

ana <- population( # Anatolian farmers
  name = "ANA", time = 15000, N = 10000, remove = 4000,
  map = map, polygon = anatolia
) %>%
  expand(by = 2500e3, start = 10000, end = 7000,
         polygon = join(europe, anatolia), snapshots = 10)
plot(ana)

eur <- population( # European population
  name = "EUR", time = 15000, N = 5000,
  map = map, polygon = europe,
  competition_dist = 400e3, mate_dist = 200e3, offspring_dist = 25e3
) %>%
  resize(N = 10000, time = 5000)
plot(eur)

yam <- population( # Yamnaya steppe population
  name = "YAM", time = 7000, N = 2000, remove = 3000,
  parent = ehg, polygon = list(
    c(26, 50), c(38, 49), c(48, 50),
    c(48, 56), c(38, 59), c(26, 56)
  )
) %>%
  move(trajectory = c(15, 50), start = 5000, end = 3000, snapshots = 10)
plot(yam)


# 3. geneflow events ------------------------------------------------------

geneflows <- list(
  geneflow(ana, yam, start = 6500, end = 6000, rate = 0.3),
  geneflow(ana, eur, start = 8000, end = 6000, rate = 0.5),
  geneflow(yam, eur, start = 4000, end = 3000, rate = 0.75)
)

# 4. model compilation ----------------------------------------------------

model <- compile(
  populations = list(ehg, ana, yam, eur), geneflow = geneflows,
  generation_time = 30, resolution = 10e3,
  competition_dist = 200e3, mate_dist = 200e3, offspring_dist = 70e3,
  dir = "/tmp/test-model", overwrite = TRUE
)

model # when printed, model objects present a brief summary

# 5. model visualization --------------------------------------------------

# open the shiny explorer (you might get a note about a website trying
# to open a popup window)
explore(model)

# 6. execution in SLiM ----------------------------------------------------

slim(
  model, seq_length = 1, recomb_rate = 0,
  save_locations = TRUE, # only record the locations of all individuals
  method = "batch", # simulate using command-line SLiM (could be also "gui")
  verbose = TRUE
)
